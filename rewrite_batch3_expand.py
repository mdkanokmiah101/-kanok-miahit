def find_content_bounds(slug):
    """Find start/end positions of content block for a slug."""
    idx = TEXT.find(f'slug: "{slug}"')
    if idx == -1:
        return None, None, None
    marker = 'content: `'
    s = TEXT.find(marker, idx)
    if s == -1:
        return None, None, None
    s += len(marker)
    e = s
    while e < len(TEXT):
        if TEXT[e] == '`':
            rest = TEXT[e+1:e+10].lstrip()
            if rest.startswith(','):
                break
        e += 1
    if e >= len(TEXT):
        e = TEXT.find('`,\n  }', s)
        if e != -1:
            e += 1
        else:
            return None, None, None
    return s, e, TEXT[s:e]


